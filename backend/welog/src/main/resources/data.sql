-- 2020년 7월 20일, 네이버 게임 라운지가 추가되었다. 업데이트 소식 관심 있는 유저들은 자신이 좋아하는 게임을 검색해 라운지에 들어가 글을 쓰고 소식을 공유할 수 있다.
-- 한마디로 게임별 커뮤니티이며 이를 한 곳에 모아둔 공간이다. 기존 게임 갤러리와는 차이가 있는데, 가령 인벤이나 헝그리앱이 ''게임에 대한 종합성''을 보여준다면 네이버 게임 라운지는 ''소통하고 공감한다는 커뮤니티 기능''에 초점을 두고 있다.[8] 해당 게임을 즐기지만 다른 곳에 있는 기존 유저 및 새로운 유저 (일명 뉴비)들에게 얼마나 활용되는가에 따라 그 흥행이 갈릴 것으로 예상한다. 네이버게임 링크를 클릭하면 이 라운지로 다이렉트되며, E-스포츠나 PC게임으로 넘어가고 싶으면 좌측 최상단에 위치한 로고를 클릭하면 된다.
-- 인물 검색: 네이버의 점유율이 높다 보니 여기에 적힌 내용이 공식 내용처럼 여겨지는 상황. 인명을 넣어 검색할 경우 역사적, 사회적으로 유명한 인물의 사진과 이력을 띄운다. 문제는 역사적으로 대한민국에 영향을 미친 인물이 아닌 현재 진행형으로 가십거리 혹은 영향력을 가진 인물부터 인물 정보로 띄운다는 것. 최고다 이순신/논란 항목을 보면 더 자세한 폐해를 알 수 있다. 결국 연예인과 기업인 위주로 인물 정보를 띄운다. 게다가 여론이 일어 지적당하기 전에는 절대로 안 고친다.
