-- 5. 국가별 이용자 분포[편집]
-- 가장 활발하게 이용하는 국가는 일본, 대만, 태국 세 개 국가다. 2020년 1분기 월간활성사용자수(MAU)를 기준으로 점유율로만 따지면 인구 2400만 명 중 2100만 명이 라인을 이용하는 대만이 가장 높고, 인구 1억 2천 중 8400만 명이 이용하는 일본이 그 다음이다. 태국은 또한 인구 7000만 명 중 4600만 명이 라인을 이용하고 있다.
--
-- 출발부터 네이버 재팬에서 일본 내 서비스를 목적으로 내놓았기 때문에 일본인 사용자가 굉장히 많으며 사실상 일본 국민 모바일 메신저의 위상이다. 일본에서 ''라인 하니?'' 혹은 ''라인 (아이디) 가르쳐줘~''라는 말이 작업 걸기의 대표적 멘트로 꼽힐 정도다. 심지어 2020년 기준으로 학생들 사이에서는 전화번호가 아니라 라인 아이디를 공유할 정도. 전화번호는 서로 모르는 경우가 생각보다 많다. 스마트폰 대중화 이후 한국에서 ''문자할게''가 아니라 ''카톡할게''라고 말하는 것과 같은 흐름. 어느 나라 보다 심한 일본의 불편하고 비싼 문자보다는[23] 편리하고 저렴한[24] 메신저로 옮겨갈 수밖에 없었던 시기에 일본에서 상호호환성 등 기능에서 앞세워 선점하여 라인이 자리잡은 것.
--
-- 또한 모바일 이메일이나 PC 통신 등에서 이모티콘을 매우 애용하기로 유명한 일본 소비자층을 사로잡기 위해 여러가지 캐릭터를 필두로 한 개성 있는 라인 스티커 서비스를 제공했는데 대박을 쳤다. 실제로 라인 스티커 관련 매출도 많고, 라인 캐릭터를 활용한 상품 코너도 일본 젊은이들 사이에선 대호황이다. 또한 봇을 활용한 한-일, 일-한 번역 기능 같은 편의기능이 다수 탑재되었으니 소비자들의 두터운 지지를 받는 게 당연한 셈.
--
-- 이러한 인기를 반증하듯 실제 일본 애니메이션, 일본 드라마 내에서 쓰이는 메신저는 대부분이 라인이다. 그래서인지 일본 애니나 드라마를 번역하는 분들이 라인에 관해 잘 모르는 한국인들의 이해를 돕고자 카카오톡으로 의역하는 경우도 종종 보였으나, 2010년대 중후반쯤 와서는 라인을 모르는 한국인도 거의 사라졌으므로 그냥 라인이라고 번역하는 편.
--
-- 대만은 일본보다 훨씬 라인 점유율이 높아서 라인 없이 일상생활이 불가능할 지경이며 라인 메신저를 플랫폼으로 다양한 사업을 전개하고 있다. 대부분 상점이 라인페이를 지원하고 있으며 카카오옐로페이지 격인 라인앳을 활용하는 상점들도 상당히 많다. 라인 플랫폼을 기반으로 하는 라인게임 사업도 활발하게 전개하고 라인모바일, 라인택시, 라인TV 등 서비스를 제공한다.
--
-- 태국에서는 아예 통신사가 ''라인이 되는 것''을 스마트폰의 기능으로 내세우는 광고를 만들기도 하였다. 물론 NHN과 협력한 것이 아닌 태국 통신사의 독자적 광고. 한국에서 스마트폰이 본격적으로 보급되고 카카오톡이 한참 인기를 얻어가고 있을 때 스마트폰 기준이 ''카톡이 되는가''였던 상태와 비슷하다 볼 수 있다. 2019년 기준 가입자는 4400만 명이다. 한편 싱가포르나 말레이시아 같은 이웃 나라들은 what''s app 사용률이 높다. 태국 경찰청 공식 메신저로 범죄자 신상조회 및 모든 경찰서비스와 관련된 업무를 라인으로 사용 중이다. 배달 및 택시 플랫폼인 라인맨 서비스는 태국에서만 제공되는 서비스이며, 교통카드인 래빗카드와 라인페이 결합 서비스 등이 제공된다.
