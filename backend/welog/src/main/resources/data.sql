-- NHN 대표였던 김범수는 2006년 11월 29일 카카오의 모태가 되는 아이위랩을 설립한다. 그리고 2007~2008년 미국에 블로그 서비스인 부루, 한국에는 소셜 추천 서비스 위지아를 내놓지만 큰 성과를 거두지 못했다. 이후 2010년 부터 카카오 브랜드를 가진 서비스를 출시하기 시작했다. 각각 카카오아지트(마이크로 카페), 카카오톡(모바일 메신저),카카오수다(마이크로 블로그) 3가지 시리즈를 선보였으며 이중 카카오톡이 대박을 터뜨려 국민 메신저로 자리매김했다. 그리고 카카오톡의 인기에 힘입어 2010년 9월 1일 사명을 ‘카카오’로 변경한다.
--
-- 한편, 당시에만 해도 ''와츠앱''이라는 카카오톡과 유사한 어플리케이션이 이미 존재하였고, 미국과 국내에 출시되어있는 상황이었으나, 해당 앱은 유료인 반면에 카카오톡은 무료 서비스였다는 점에서 차이가 있었고, 이러한 점이 후발주자임에도 불구하고 국민 메신저로 자리매김하는데 큰 요인이 되었다.
--
-- 그리고 이런 무료 서비스를 유지하기 위해, 카카오톡의 초기 창업 자본금은 100억[10]이었으나 2011년까지 2년간 210억원의 누적적자를 보게 되었다.[11] 또한 이때만 하더라도 카카오톡은 뚜렷한 수익 모델이 없었기에 많은 사람들이 우려를 표하였다. 그러나 2012년 게임업체인 애니팡 및 몇몇 중계서비스 업체와 계약을 체결하며 90억의 영업이익을 달성하였고 이후 텐센트, 위메이드 엔터테인먼트(국내 온라인 게임사)로부터 각각 720억원(360만 주, 지분율 12.38%), 200억원(100만 주, 3.44%)을 제3자 유상증자 형태로 투자 받으며 간신히 기사회생하였다.기사 이후 카카오톡은 앱 개발에 전념하며, 광고비에 공격적으로 투자하기 시작하여[12] 2013년에는 550억의 수익을 달성하게 된다.
