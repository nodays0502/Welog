-- 6.2.1. 외국한정 스티커 한국에서 구매하는 법[편집]
-- 국내 사용자가 외국에서만 판매하는 스티커를 구매하는 방법엔 다음과 같은 두가지 방법이 있다.
-- 6.2.1.1. 현재 가능한 방법[편집]
-- 요새는 기존에 가능했던 직접 구매 방법이 죄다 막히면서 아래 방법밖에 남지 않았다. 쉽게 말해 직접 구매하는 방식은 더 이상 불가능하다.
-- 외국에 있는 지인에게 선물해 달라고 부탁하는 법
-- 일본인 지인이나 일본에 거주중인 지인이 있으면 이 방법을 시도해보자. 대부분 가격 그대로 받거나 운이 좋으면 아예 안받을수도 있어 아래에 서술한 구매대행보다 싸게 먹힌다.
-- 구매대행
-- 일본에 있는 업자에게 스티커 가격에 수수료를 포함한 금액을 선입금 후 선물을 해 주는 방식이다. 단점은 수수료의 가격이 스티커 가격과 똑같거나 더 비싼 경우가 대부분이며, 거래의 대부분이 SNS를 통해 이뤄지며 선입금의 특성 상 돈만 받고 먹튀할 가능성이 높으며, 이러한 사기 사례도 많으니 주의하자.
-- 6.2.1.2. 더 이상 불가능한 방법[편집]
-- 전화번호 등록을 안 한 상태에서 VPN을 이용해 해당국가 IP로 우회한 뒤 구매 하는 법.
-- 여기서 주의할 점은 절대 한국 전화번호를 등록해선 안된다! 한국 전화번호 등록을 하면 영원히 국적이 한국으로 고정되어 VPN을 쓰더라도 그 지역 스티커를 절대 구매할 수 없다. 더 나아가 라인 페이 등 내수용 서비스도 일체 이용할 수 없으므로 꼭 필요하지 않다면 전화번호 등록을 아예 하지 않는게 편하다. 만약 이렇게 되었다면 계정을 새로 만드는거 말고는 답이 없다. 일본 IP로 가입한 계정을 한국 IP로 접속하면 일본한정 스티커 구매가 불가능한데 다시 일본 IP로 접속하면 일본한정 스티커를 구매할 수 있다. 그러나 라인측에서 VPN IP를 속속 차단하더니 2019년 현재 VPN을 사용하여 스티커를 구매하는 방법이 대부분 막혔다. 가끔 듣보잡 VPN으로 시도하면 뚫리기는 하나 이 역시 추적당해 금방 차단하는 듯 하다. 2020년 현재 VPN을 통해 라인 스토어 접속까지는 가능하지만 전화번호를 등록하지 않은 계정은 이용할 수 없다면서 구매 단계에서 막히며 한국 전화번호를 등록한 계정은 VPN을 켜도 한국 오직 한국 스토어로만 접근된다.
-- 현지에서 선불SIM을 사용하여 현지 전화번호를 등록하여 구매하는 법
-- 전화가 가능한 선불SIM카드로 현지 전화번호를 등록하는 방법이 있지만 차피 버릴 라인 계정이라면 상관 없지만 공식적으로 사용하는 라인 계정에 이 방법을 사용하는건 절대 권장하지 않는다. 사용이 끝난 선불SIM의 전화번호를 추후 다른 사용자가 라인 전화번호 변경 등록을 할 경우[27] 기존에 쓰던 라인에서 로그아웃되거나 영구정지 당한다. 일본으로 예를 들면 로그아웃 될 경우 로그인 하기 위해 반드시 새로 전화번호 인증을 해야 하는데 위에서 말한것 처럼 반대로 일본 전화번호를 등록하면 일본 국적으로 영구 고정되기 때문에 일본의 전화번호만 등록이 가능하게되어 한국에선 로그인을 할 방법이 불가능해진다. 즉 계정을 다시 되살리려면 일본에 가서 선불SIM을 구매해야 하는 악순환을 반복해야 한다. 결국 이렇게 해도 언제 로그아웃될지 모르는 시한부 인생을 살게되어 계정을 제대로 사용하지 못하게 된다. 참고로 국적이 일본으로 고정되었기 때문에 한국 전화번호로 변경조차 되지 않아 일본 전화번호로밖에 변경을 하지 못한다. 계정이 이렇게 되어 로그인을 하지 못해 한국 라인 고객센터로 전화를 해도 한국 계정이 아니라 자기네 관할이 아니라고 일본 라인으로 문의하라고 한다. 과거 가능했던 방법으로는 텍스트플러스 같은 가상 일본 전화번호를 얻거나 일본 임대폰을 이용해서 일본 전화번호를 등록하는 방법이 있었으나 이 역시 임대, 가상전화번호 대역을 역추적당해 2017년에 죄다 막혔다. 선불SIM 전화번호 대역도도 전부 인식하는지 전화번호 등록 시도시 사용할 수 없는 번호라고 뜨면서 막힌다.
