-- 3D TV
-- 삼성전자는 3D 입체영상을 즐길 수 있는 3D TV를 출시 6개월 만에 100만대를 판매, 시장 주도권을 굳히고 있다. 이는 2010년 초 시장조사기관이 예측했던 전 세계 3D TV 수요 123만대에 근접한 수준이다. 안경이 필요 없는 3D TV도 개발 중이다.[22] 삼성전자는 3D 입체영상과 프리미엄 입체음향을 동시에 즐길 수 있는 3D 홈시어터(HT-C6950W)도 처음으로 내 놓았다. 삼성전자는 3D 홈시어터까지 출시함으로써 3D TV뿐만 아니라 3D 블루레이 플레이어•3D 콘텐츠•3D 안경 등을 총 망라한 ''3D 토탈 솔루션''을 구축하고 3D 엔터테이먼트 환경을 업계 최초로 완성했다고 말하고 있다.[23]
--
-- 삼성전자는 3D TV에 3D 컨텐츠 스트리밍을 제공할 계획이다. 자체 온라인 앱스토어를 가진 삼성전자 3D TV는 훌루 플러스 및 냅스터 등의 프로그램을 사용자들이 직접 TV의 하드 드라이브에 다운로드할 수 있게 했다. 또한 삼성 앱스토어는 개발자들이 실제로 새로운 앱 플랫폼으로부터 돈을 벌 수 있는 프리미엄 컨텐츠를 추가할 예정이다. 또한 앱스토어를 통해 3D 컨텐츠도 제공할 계획이다.[24]
