-- 삼성 UHD TV
-- 세계 TV 시장에서 삼성전자는 몇 년째 부동의 1위를 차지하고 있으며 연이어 베스트셀러 제품들을 내놓고 있는 히트 제조기다. 2009년 삼성전자는 3,100만 대의 평면 TV를 판매해 4년 연속 세계 시장 점유율 1위를 유지했다. 2010년 초 삼성전자는 LED TV 1,000만 대와 더불어 총 3,900만 대의 TV를 판매하는 것을 목표로 세웠다가 다시 이를 28%나 늘어난 5,000만 대로 높여 설정했다 삼성전자는 핑거 슬림의 디자인 혁명이라 일컬어지는 LED TV로 한발 앞서 새로운 시장을 창출하고 있다. 2009년 3월 세계 최초로 선을 보인 LED TV는 260만 대를 판매해 신시장 창출에 성공했다.삼성전자는 2006년 LCD TV, 2009년 LED TV 등 해마다 밀리언셀러 TV 제품을 탄생시키며, ‘5년 연속 밀리언셀러'' 히트 계보를 이어 가고 있다. 삼성전자는 2010년에도 8월 중 3D TV ''100만대 판매''를 달성해 이 계보를 이었다. 삼성전자는 2009년부터 2014년까지 5년 연속 글로벌 점유율 20%를 넘었다.[20]
