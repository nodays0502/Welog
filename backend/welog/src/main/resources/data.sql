-- 미국 애니메이션 관련 스티커도 꽤 되는데, 일본에 디즈니랜드가 있어서 그런지 겨울왕국이나 미키마우스 같은 디즈니 관련 상품이 대다수. 최근에는 파워퍼프걸이나 검볼, 어드벤처 타임과 같은 카툰네트워크 작품 관련 스티커도 추가되고 있다.
