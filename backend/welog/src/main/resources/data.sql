-- 휴대전화
-- 삼성전자에서는 스마트폰 분야에서 갤럭시 브랜드를 사용하는 휴대폰을 대표 상품으로 내놓고 있다. 스마트폰은 삼성전자의 전체 매출의 60%와 순이익의 약 70%의 비중을 차지하는 가장 중요한 사업이기 때문에, 삼성전자의 전망과 주가는 갤럭시 시리즈의 판매 실적에 따라 움직이는 경향이 있다.
--
-- 2013년을 정점으로 판매 호조를 보이던 스마트폰 사업은 후발주자의 공세 등으로 2014년 들어 실적이 급감했고, 때문에 앞으로의 전망 또한 다소 어둡다.[4] 중저가폰 시장에서는 화웨이나 샤오미 등 중국 업체의 가파른 성장에 의해 더이상 점유율을 올리기 어렵게 되고 있고, 높은 가격의 폰 시장에서는 애플의 아이폰 시리즈와 대결해야하는 상황이라 다른 기업들에게 끼여 있는 상황이다.
--
-- 가전 및 반도체
-- 삼성전자의 제품은 텔레비전 등의 음향영상기기(AV), TV, 냉장고, 세탁기, 에어컨, 가전제품, 스마트폰 등 각종 휴대전화, 컴퓨터, 하드디스크 등의 컴퓨터 및 주변기기에서부터 반도체에 이르기까지 그 종류가 매우 다양하여, 소수의 제품에만 역량을 집중하는 애플과 대비되고 있다. 가전제품 중 매출의 비중이 큰 것은 LED TV를 비롯한 다양한 TV 제품들이다.
--
-- 삼성전자의 반도체는 전세계적으로 TV와 휴대폰, 컴퓨터는 물론 각종 전자제품에 광범위하게 사용되고 있으며 경쟁사인 애플도 삼성전자에서 메모리를 공급받고 있다.[5] 반도체 중 DRAM의 시장점유율은 2017년 3분기 기준 44.5%로 세계 1위를 지켜오고 있다. 27.9%로 2위를 차지한 SK하이닉스까지 합하면, 한국 기업의 DRAM 시장점유율은 72.3%로 압도적이다.[6]
