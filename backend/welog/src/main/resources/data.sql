-- 3.5.1. 주의사항[편집]
--
-- 배달 시 주의할 점과 팁이 있다. 우선, 하술할 모든 주의사항에서 필요한게 주문 상세 확인이다. 주문 상세 내역은 배달에 필요한 정보를 모두 가지고 있으므로 콜이 들어올 때마다 매번 주문 확인을 하는 습관을 들여놓자.
--
-- 첫 번째는 현장 결제 주문의 경우 결제 방식 확인이다. 카드 결제는 단말기가 없어도 카드 번호를 입력해 수월하게 결제할 수 있지만, 문제는 현금 결제인 주문이다. 잔돈을 가지고 다닌다면야 아무런 문제가 없지만, 현금이 없는 상태에서 현금 결제 콜을 받으면 결제 시 거스름돈을 줄 수 없어 곤란해진다. 웬만하면 주문자들이 금액을 맞춰서 주기도 하고, 또는 주문자에게 금액을 맞춰서 달라고 말하면 되지만, 주문자도 금액을 맞춰서 줄 수 없을 때는 난처해진다.[16] 마지막 방법으로 계좌이체로 거스름돈을 송금해주면 되지만, 어쨌든 현금 결제 주문인 것만 확인하면 이런 여러모로 불편한 상황을 피할 수 있으니 확인해야 할 필요성은 충분하다. 그러므로 콜을 받기 전 결제 방식을 반드시 확인하도록 하자[17].
--
-- 대면 카드 결제시 단말기가 없으면 지역화폐카드 결제나, 2개 이상의 카드로 분할 결제를 요구하는 손님들에게 대처가 불가능하다. 단말기가 없다면 대면 카드 결제는 받지 않는 것이 좋다.
--
-- 그리고 주문 내역을 확인하여 자신이 한 번에 소화할 수 있는 양인가 꼭 확인해야 한다. 예시로 10만원어치 이상의 음식이나 B마트 주문.[18] 저 정도의 주문은 커넥터들이 들고다니는 크기의 가방에는 웬만해서 들어가지 않는다. 한 번에 배달을 마쳐야 하는데, 손으로 들고 갈 수 있는 거리도 아니라면 답이 없다. 이와 같이 배달이 어려운 상황에 처하면 지체하지 말고 관제 센터에 도움요청을 하자.
--
-- 대량 주문 등 특수한 경우에는 보상 배차를 받을 수 있는데[19], 그 조건은 상품가액 10만원 이상의 대량 주문, 주문액과 무관하게 음료 13잔 이상, 엘리베이터 고장, 고객 주소 오기재[20] 중 해당사항이 한 개라도 있으면 된다[21]. 회사측에서 알아서 보상 배차를 해주지 않기 때문에 본인이 알아서 챙겨야 한다.
--
-- 몇몇 가게는 커넥터들의 배달이 너무 느려서, 또는 도보/자전거/킥보드로는 배달하기 힘든 음식이라는 등의 이유로 커넥터가 배차받는 것을 기피해 가게 정보에 커넥터들은 배차를 받지 말라고 따로 표시를 해놓는다. 문제는 이 메시지가 가게 위치 팁 창에 적혀져 있어 보지 못하고 받게 되는 경우가 종종 있다. 커넥터를 기피하는 가게 중 대다수는 정말로 커넥터 가방으로 배달하기 힘든 음식을 파는 가게인 경우가 많으므로 받지 않는게 좋다.
--
-- 위 주의사항의 연장선으로, 음식점 콜이 들어왔을 때, 무슨 음식을 배달해야 하는지 보고 수행이 어렵다 싶은 배달은 적절히 거르는 센스도 필요하다. 대표적으로 피자는 내용물이 망가지지 않게 하려면 배달 내내 수평을 유지하고, 흔들림을 최소화 해야하지만, 커넥터들의 보편적인 운송수단과 가방으로는 아주 힘들다. 억지로 배달하다 피자를 못먹게 만들어 버리면 배상해야 함은 물론, 배달료도 못받는다. 이와 같은 이유로 피자 주문은 거절하는 것이 본인과, 주문자, 가게, 모두에게 좋다. 한 가지 예시를 더 들자면 커피와 같은 액체류다. 내용물이 넘치지 않게 포장을 제대로 해주는 가게라면 괜찮지만, 그렇지 않은 가게는 이동 중 내용물이 새어 나올 가능성이 높으므로 음료 배달도 수락에는 신중을 기해야 한다.
--
-- 물건/음식을 받으면 포장 상태를 잘 확인하자. 비닐/종이백에 구멍이 뚫려있거나, 박스가 찌그러져 있거나, 음식물이 포장 내외부에 묻어있는 등[22], 포장 상태가 나쁘다고 보일 때는 바로 가게측에 재포장을 요구해야한다. 가게 측의 실수로 포장이 제대로 되지 않은 물건을 배달하다 내용물이 훼손됐을 때는 변상하지 않아도 되겠지만, 업주가 과연 자기들이 포장을 잘못했다고 순순히 인정할지는 미지수. 그러므로 픽업을 하면서 포장에 이상이 없는지 업주/직원이 있는 자리에서 꼼꼼히 확인하고 출발하자. 규정 외의 방법[23]으로 배달을 수행하다 상품을 훼손했을 때와 같이 배달원에게 명백하게 책임이 있을 경우에는 우아한형제들 측으로 상품가액을 송금하여 변상해야 한다[24].
