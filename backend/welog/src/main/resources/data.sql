-- 그런데, 2013년 10월 8일에 결국 패키지 게임 서비스 종료가 발표되었다. 기존에 네이버에서 구매했던 게임은 네이버 캐쉬 또는 스팀용 키로 대체되고 스팀 시디키 판매를 새롭게 시작할 예정이라고 한다. 2014년 8월 11일, 예고한 대로 스팀 키 판매 서비스를 시작하였다.(#) 아직은 등록된 게임의 수가 많지 않지만 차차 늘려갈 것으로 보인다. 그러나 2015년 3월 31일 화요일 오후 3시 스팀 키 판매 서비스도 종료한다고 한다. 단, 기존에 스팀키를 구매한 경우 마이페이지에서 조회가 가능하며 이미 구매한 스팀키는 클라이언트를 통해 계속 사용할 수 있다고 한다.
