-- 일단 네이버 측에서는 NHN 재팬에서 개발되었다고 발표하고 있다. 한국의 NHN과는 별도로 NHN 재팬(나중에 ''라인 코퍼레이션''으로 사명 변경)에서 개발했으며, App Store에도 네이버 재팬 이름으로 올라와 있었다. 구글 플레이에는 초기에는 App Store처럼 개발사가 네이버 재팬이라고 나와 있었으나 이후 한국에도 진출하면서 한국의 네이버에서 나온 앱은 NHN Corporation으로, 일본의 네이버 재팬에서 나온 앱은 그냥 NAVER를 달고 나오고 있다. 음성통화 기능은 한국의 NHN에서 개발.
--
-- 개발 일화가 있다. 2011년 도호쿠 대지진 당시 일본인들이 전화가 먹통이 된 환경에서 핸드폰의 트위터 같은 소셜 네트워크 서비스를 이용하여 서로의 안부를 확인한 경우가 많았는데,[4] 네이버의 창업자 이해진이 그걸 보고 아이디어를 내어 일본지부인 NHN 재팬에 개발지시를 내린 것.[5][6] 이때 NHN은 옛날부터 하던 일본 진출 사업을 계속 죽쑤던 상황이었는데, 이 앱의 개발이 네이버로선 완전히 전환점이 되었다. 헤럴드코리아 여담으로 이런 개발시작 배경 때문인지, 라인 메신저는 서버가 튼튼하기로 유명하다. 연말연시는 당연하고 지진 발생시나 그 외에도 시도때도 없이 먹통이 되는 카카오톡과는 달리, 1억명이 넘게 사용하는 라인은 연말연시든 지진 발생시든 서버가 멀쩡하다. 물론 지진으로 인해 그 일대 통신망이 망가져 통신이 불가능한 경우는 있지만, 라인의 문제로 메세지 발송이 불가능한 경우는 거의 찾아볼 수 없을 정도.
