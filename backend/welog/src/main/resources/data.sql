-- 삼성전자는 2010년 반도체에 대한 투자 규모를 11조원으로, 특히 메모리 반도체 시설투자를 당초 계획했던 5조5,000억원에서 9조원으로 늘렸다. 이 같은 움직임은 2010년에 삼성전자가 지속적으로 이들 부문에서 세계 최대 공급업체의 지위를 유지할 수 있게 할 전망이다.[36]
