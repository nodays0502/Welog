-- 창업주 이병철은 회사 내의 모든 이들의 반대를 무릅쓰고 1983년 일본 도쿄에서 반도체 사업에 본격적으로 뛰어들었다. 이병철은 삼성은 자원이 거의 없는 한국의 자연조건에 적합하면서 부가가치가 높고 고도의 기술을 필요로 하는 제품을 개발하는 것만이 제2의 도약을 기할 수 있는 유일한 길이라고 확신한다고 반도체 사업 진출 이유를 밝혔었다. 삼성전자는 그로부터 불과 10개월 만에 세계 3번째로 64K D램을 내놓음으로써 반도체 업계는 물론 한국 내외 경제계를 놀라게 했다. 그러나 이후 반도체 가격 폭락으로 사업초기에 어려움이 컸다. 이런 악조건 속에서도 삼성의 메모리 반도체는 눈부신 성장을 거듭했고, 1992년 64M D램을 최초로 개발, 마침내 세계 최고의 기술력을 확보했으며, 1993년에는 드디어 메모리반도체 세계 1위로 올라섰다. 1994년, 1996년 256M과 1G D램을 연속 최초 개발, 반도체를 한국의 대표산업으로 키웠다. 2002년에는 낸드플래시 세계 1위에 올랐으며 2006년 세계 최초 50나노 D램과 2007년 30나노 낸드 등을 최초로 내놓으면서 삼성전자는 메모리업계 점유율 30%가 넘는 절대강자로 군림하고 있다.[34]
--
-- 삼성전자는 2010년 들어서도 ‘세계 최초’ 기록을 계속 이어가고 있다. 30나노급 D램 양산도 삼성전자가 처음이다. 삼성전자는 30나노급 2Gb DDR3(Double Data Rate 3) D램을 7월부터 양산하기 시작했다. 30나노는 머리카락의 4천분의 1정도의 굵기 정도다. 30나노급 D램은 기존 40나노급 D램보다 60% 정도 생산성이 높다. 원가 경쟁력도 50~60나노급 D램의 2배 이상이며 소비전력도 50나노급 D램보다 최대 65% 이상 줄일 수 있다.
--
-- 시장조사기관 가트너에 의하면 D램의 호조세로 삼성전자는 2010년 2/4분기 D램 순위에서 시장점유율 선두를 고수했다. 가트너는 보고서에서 삼성전자는 성장세를 타고 35%가 넘는 시장점유율을 기록해 그 위치를 확고히 했다고 전했다.[35]
