--
-- 다만 직접적인 개발은 한국 개발팀에서 했다는 말도 있다. [8]이 말에 따르면 네이버톡을 개발한 한국의 개발팀이 네이버톡 소스코드에서 당시 커뮤니케이션 캐스트라 불리던 SNS부분을 제거하고 메신져 부분만 남긴 채 라인 스킨을 입혀서 초기 버전을 출시한 것이 라인의 시초라고 한다. [9]일본 라인 팀의 이나가키 아유미를 포함한 일본 라인 팀이 스킨 개발에 참여하는 등 UX를 담당하긴 했지만 직접적으로 제작하진 않았단 것. [10] 개발 이유도 다른데 2010년 네이버톡 기획 당시 이해진 CSO는 항상 입버릇 처럼 우리가 그동안 잘 해온 데스크탑의 경험을 그대로 모바일로 옮겨야 한다고 주장하여 네이버톡에 커뮤니케이션캐스트라는 그 당시 스마트폰 스펙[11]으로는 감당하기 힘든 스펙으로 개발하자고 했으나 개발팀은 이런 장황한 스펙은 당시 스마트폰에선 무리이고 메신저에 집중해야 한다고 여러차례 의견을 표명했음에도 기획측에서는 이해진 CSO의 지시이므로 절대 양보가 불가능하다고 해서 어떻게 개발하긴 했는데 메모리가 부족해지거나 속도가 느리게 출시되는 참사를 맞아 네이버재팬 측에서 내건 ''기존 개발팀을 유지하되 이해진 CSO가 기획에는 일절 간섭하지 않는다''는 조건으로 네이버톡에서 메신저 기능만 남겨놓은 라인 프로젝트를 시작했다고 한다.[12] 하지만, 라인은 시작부터 일본 시장을 겨냥한 관계로, 일본의 반한 정서를 감안해 한국에서 개발되었다는 사실을 철저히 숨겨왔다고 한다.
-- 2.1. 애플리케이션 국적 논란[편집]
-- LINE 매출의 절반은 일본에서 발생하며 또한 일본에서 가장 점유율이 높은 메신저도 LINE이다. 입헌민주당이나 자유민주당이나 라인에 계정을 가입하고 총리 관저나 내각 홍보실 등에서도 활용하고 있다. 즉, 한국의 카카오톡의 위상과 동일한 것이 일본의 라인이다. 일본 말고도 대만과 여러 동남아 국가에서도 라인을 많이 이용하는데 동남아 국가에서는 태국과 인도네시아에서 점유율이 높다. 2019년 기준 카카오톡의 한국 내 월간 사용자 수가 4400만 명, 한국을 포함한 전 세계 사용자 수는 5000만 명 정도인 것과 비교해 보면 라인의 사용자 수는 1억 6천만여명이다. 다만 둘 다 전 세계 시장을 놓고 보면 점유율이 높지 않은 수준으로 태국을 제외한[13] 동남아의 경우 페이스북 메신저와 왓츠앱이 시장을 장악했다.
--
-- 하지만 일본 스마트폰 시장 점유율 1위가 iPhone이라고 Apple이 일본 기업이 되지는 않으며, 삼성전자의 미주지역 매출이 한국 매출을 능가한다고 해서 삼성전자가 미국 기업이 되지는 않듯이 점유율이나 주요 시장이 LINE의 국적을 결정짓는 요소는 될 수 없다. 그렇다고 모회사인 한국의 네이버가 주관하는 것도 아니며, 한국 국적이라고 할 수도 없다. 라인의 모회사 네이버가 외국인 주주비율 60%를 넘는 다국적 기업이기 때문에 무국적 기업이 된다는 주장도 있으나 네이버의 최대주주는 국민연금공단이다. 아예 다국적 기업이 많아진 요즘 세상에 기업 국적을 따지는 의미가 없다는 반론도 가능하지만 그럴 경우 이 문단을 읽지 않으면 된다.
--
-- LINE에서는 자사가 ''한국 기업''이라고 홍보하지 않는다. 한일관계 특성상 비즈니스적인 고려도 있었겠지만 Microsoft나 구글의 현지 자사도 자사가 미국 기업이라고 굳이 크게 써붙이지 않기 때문에 색안경을 끼고 볼 필요는 없다. 일본에서는 라인을 자국 기업으로 인식하는 경우도 많았지만 2014년 초 일본의 웹진 비지니스저널[14]에서 한국 국가정보원이 네이버 라인을 도청하고 있다는 5ch발 루머가 기사화되면서 국적논란이 대두됐다. 이는 한국 공안당국이 지메일 쓰면 종북친미종북[15]이라는 해괴한 논리를 영장청구에 포함시키는 등 지메일 도청을 위해 힘쓰고 네이버, 카카오톡 등이 영장에 근거하지 않고 임의제출 형식으로 사용자의 개인정보를 제공하는 등 실질적인 사례를 바탕으로 하므로 이를 악의적 유언비어로만 보기에는 현실적으로 무리가 있는게 사실이다. 상세는 사이조(잡지) 문서로.
--
-- 라인 내에는 한국인 임직원이 많은 편이고 한국인 임원들의 보수는 매우 높은 편에 속한다. 일본의 시사 주간지 도요게이자이(東洋経済) 2018년 9월 기사에 따르면, 일본 국내 임원보수총액랭킹(회장, 사장 포함)에서 라인의 한국인 신중호 이사(取締:토리시마리;토리시마리役야쿠의 준말)가 13위, 황인준 이사가 89위를 기록했다. 사내에 일본인 직원을 대상으로 한 한국어 강좌도 있고 반대로 한국의 네이버로 출장오는 일본인 직원들도 있다. 외국인 직원들이 많고 벤처기업을 표방하고 있기 때문에 분위기도 여타 일본 IT 기업에 비해 자유롭고 젊다는 평판이 나있다. 원래부터 모회사인 네이버의 영향력이 없지는 않았지만 2016년 라인 상장 이후 네이버의 영향력이 더 강해졌고 2019년부터는 신중호 이사가 공동 대표이사로 취임한 뒤 스톡옵션을 3번째로 챙겨갔다.
--
-- 라인 메신저라는 LINE의 제품이 한국 태생이냐 일본 태생이냐를 세부적으로 따지는 경우도 있을 수 있는데, 문자 그대로 어디 ''태생''인지를 묻는다면 LINE이 위치한 일본 땅에서 개발이 되었기 때문에 일본 태생이다. 당연한 소리지만 기획, 개발을 주도한 쪽은 네이버 출신의 한국인 임직원이다. 사람으로 비유하자면 한국계 일본인이라고 볼 수 있을 듯.
