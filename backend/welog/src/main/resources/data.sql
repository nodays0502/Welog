-- 스마트 TV
-- TV 부문에서의 큰 특징 중 하나는 ‘보는 TV''에서 ''즐기는 TV''로의 변화다. 기존에 수동적으로 TV를 시청하던 소비자들은 보다 적극적이고 다양한 콘텐츠를 스스로 선택하기를 원하고 있다. 삼성은 이같은 수요를 파악하며 2007년 인터넷TV 출시로 TV를 시청하면서 실시간 인터넷 정보를 동시에 즐길 수 있도록 했다. 여기에 애플리케이션을 다운로드할 수 있는 이른바 ''스마트 LED TV''도 개발하고 있다. 2008년에는 ''Power Infolink'', 2009년에는 위젯 기능으로 더욱 새로워진 ''인터넷@TV''로 시장을 주도했다. 2010년에는 3D TV 출시와 함께 새롭게 선보인 ''인터넷@TV 2010'' 기능으로 이용자가 뉴스•날씨•증권•UCC•영화 등 기존 인터넷 서비스 외에도 삼성전자의 세계 최초 멀티디바이스 애플리케이션 스토어인 ''삼성 앱스''의 애플리케이션을 다운로드해서 사용할 수 있게 하고 있다. 삼성 앱스는 2010년 하반기부터 한국과 미국을 시작으로 내년에는 유럽지역까지 유료 프리미엄 서비스가 확대 실시됨에 따라 소비자들에게 국가별로 특화된 로컬 콘텐츠 서비스를 제공할 계획이다.[21]
