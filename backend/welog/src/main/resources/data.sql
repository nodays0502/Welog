-- 3. 특징[편집]
-- 일본의 다양한 휴대전화 환경으로 인해 유효한 전화번호 없이도 스마트폰 어플리케이션에서 아이디나 페이스북 계정 형태로 가입 가능하다. 한국어판은 과거 네이버 아이디도 연동 가능했으나 현재는 서비스 종료. 스마트폰을 보유하지 않은 경우 계정을 만들기 어렵지만, 지원범위는 상당히 넓어 일단 만들어진 계정은 초기부터 블랙베리 OS 10을 제외한 웬만한 플랫폼에서 다 사용할 수 있었다.[17](하지만 지금은 iOS 12.0, 안드로이드 7.0 이상만 가능) 심지어 WAP를 통해 피처폰에서도 사용이 가능했을 정도. 그때 한국의 휴대폰 제조사는 피처폰 생산을 중단했지만, 일본은 여전히 많은 피처폰을 판매하고 사용한다.[18]
--
-- 이러한 다양한 상호호환성은 스마트폰 태동기에 라인이 폭발적 성장을 할 수 있도록 큰 도움을 주었다. 스마트폰 전환이 늦었던 일본은 물론이며 구매력이 떨어져 안드로이드 스마트폰으로의 전환이 늦었던[19] 동남아권의 경우에도 이러한 상호호환성이 바탕이 되었기에 초창기 라인이 카카오톡보다 앞서 갈 수 있었다.[20]
--
-- 윈도우용은 PC를 대상으로 한 데스크탑 버전과 폰이나 태블릿을 대상으로 한 Modern UI 버전 두 가지가 있었는데, Windows 10에서 데스크탑과 Modern UI가 통합되면서 두 버전 모두 동일한 OS에서 별개의 프로그램으로 중복설치가 된다. 두 버전 모두 각자의 방향으로 꾸준한 업데이트가 제공되고 있으며 기능에도 큰 차이는 없다.
--
-- Windows PC판의 경우 다른 데서 로그인을 할 경우 설정해 놓은 자동 로그인이 풀린다는 문제점이 있다. 예를 들어 집의 PC에서 자동 로그인을 설정해 둔 뒤 회사 PC에 로그인을 하면 집 PC의 자동 로그인 설정이 풀려 버린다. 우회할 수 있는 방법이 있는데 한쪽 PC에 구글 크롬을 설치한 뒤 크롬 OS용 라인 앱을 설치한 뒤 로그인하면 다른 PC의 자동 로그인이 풀리지 않는다.
--
-- 과거 다른 메신저 앱에서는 별 달리 다루지 않았었던 이모티콘 캐릭터를 주요 특징으로 하고 있었다. 이 스티커 캐릭터들의 디자인은 한국인 강병목 디자이너가 했다. 다른 작품과 콜라보레이션을 진행할 정도로 라인에서 상당히 신경쓰는 부분이다. 하지만 이후 카카오톡에서 카카오프렌즈를 출시하면서 다른 메신저 앱에서 이모티콘 캐릭터를 별 달리 다루지 않았다는 건 옛말이 되었다.
--
-- 이후에도 라인 크리에이터스 스튜디오를 통해 개인 개발자들까지 자유롭게 스티커를 출시할 수 있게 하면서 엄청난 수의 스티커가 쏟아져나왔고, 특히 일본에서 가장 점유율이 높다는 특징 때문에 카카오톡에 비해 서브컬쳐 계열 스티커가 월등히 많다.
--
-- 생방송 기능이 있다. 니코니코 동화 등 일본의 토종 서비스들의 품질이 심히 괴악한지라, 최근에는 라인 라이브나 유튜브 등으로 이동하는 추세. 특히 예능인이나 성우 등 유명인들 또한 이들 소속사의 적극적인 플랫폼 이주 의욕으로 라인 라이브에서 생방송을 제공하기도 한다.
--
-- 카카오톡에 비해 상당히 빨리 아이패드를 지원했다. 현재는 라인도 다기기 로그인을 지원하지 않지만 아이패드용은 PC용과 비슷하게 분류되는지 스마트폰에서 로그아웃하지 않고도 연동하여 쓰는 것이 가능하다. 물론 안드로이드 태블릿은 안중에도 없다... 라인을 주로 쓰는 국가가 일본이고 거긴 ios점유율이 높은데다가 안드로이드 태블릿은 삼성을 제외하면 똑바로 만들줄 아는 회사도 없고 나머지 국가들은 태블릿의 시장 점유율이 낮다 보니 지원할 필요성을 못느낀듯하다. 그래도 안드로이드 운영체제를 위한 경량화 버전인 라인 라이트가 있다. 라인 라이트는 라인의 각종 부가기능은 물론 대화 복구조차 못하는 아주 가벼운 앱이지만 이 앱의 진정한 존재가치는 안드로이드 태블릿에서 빛을 발하는데 무려 주기기에 설치된 라인과 별도로 로그인을 할 수 있다. 아이패드만큼은 아니지만 최소한 대화정도는 태블릿으로 가능하다.[21]
