-- ''배달의민족''은 디자이너 출신인 김봉진 대표가 설립한 스타트업 ''우아한형제들''에서 개발했으며, 애플리케이션은 2010년 6월에 먼저 출시되었고, 회사는 애플리케이션이 출시된 지 5개월 뒤에 설립되었다. 해당 브랜드를 다룬 책 『배민다움』(홍성태 지음, 북스톤 출판)에선 이름의 이유를 젊은 친구들이 이것저것 많이 배달 음식을 시켜먹는다는 의미에서 배달의 민족이라고도 하고 밝은 땅에 사는 민족이라는 중의적 의미가 있죠라고 밝혔다.
