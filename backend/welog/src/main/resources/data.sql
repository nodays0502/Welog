-- 삼성 노트북3
-- 삼성전자의 패널은 ‘더욱 얇게’ 기술 개발의 퍼레이드를 벌이고 있다. 2009년에는 세계에서 가장 얇은 두께인 3.9mm의 40인치 LED TV용 초슬림 패널 개발에 성공했다. ''Needle Slim''으로 이름 붙여진 이 패널은 두께가 약 50mm인 기존 LCD 패널의 약 1/12 수준이다. 삼성전자는 패널의 두께를 크게 줄이면서도 풀HD급 해상도, 120 Hz 구동, 5000:1의 명암비 등 고화질 성능은 그대로 유지했다. [25] 삼성전자는 2007년 10월 두께 10mm인 40인치 LCD TV 패널을 처음으로 발표하면서 10mm 벽을 최초로 돌파했으며, 2008년 10월에는 다시 7.9mm 두께를 기록하며 세계 최초 기록을 이어 왔었다. [26]
--
-- 삼성전자는 LCD TV용 패널과 함께 24인치 LCD 모니터용 패널과 12.1인치 노트북용 초슬림 LCD 패널도 개발해, 초슬림 LCD 패널 기술을 주도하고 있다. 모니터용 패널은 두께가 3.5mm이며, 노트북용 패널은 두께가 1.64mm에 불과하다. [25]
