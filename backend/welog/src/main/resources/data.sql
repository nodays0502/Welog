-- 일반 배차 - 다른 배달대행처럼, 자신 주변의 배달이 모두 뜨며 거기서 콜을 선택하여 수행하는 방식이다. (전기)자전거, 킥보드 기준 2배차까지 잡을 수 있다.[12] 모두 뜬다고 해서 전부 배차받을 수 있는건 아니고, 자전거/킥보드 기준으로 현재 자신의 위치에서 픽업지까지의 거리가 직선거리로 2km 미만인 주문만 받을 수 있다. 선호하는 배달 경로가 모두 비슷하기 때문에, 수강신청을 방불케 하는 콜잡기 전쟁이 일어날 가능성에 대비하여, 단독 추천배차라는 프로그램이 도입되어 있다. 콜 오른쪽에 타이머가 표시된 콜이 있는데, 이것은 단독 추천 배차이다. 추천이 시작된 지 15초동안 자신에게만 보여지며, 그 이후부터는 모든 사람에게 표출된다.
